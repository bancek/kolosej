module.exports = function( grunt ) {
  'use strict';

  grunt.loadNpmTasks('grunt-less');
  grunt.loadNpmTasks('grunt-browserify');

  grunt.registerTask('compass', function() {
      grunt.task.run('less');
  });

  grunt.registerTask('xwatch', function() {
      grunt.task.run('browserify less watch');
  });

  grunt.registerTask('reload', function() {
      
  });

  //
  // Grunt configuration:
  //
  // https://github.com/cowboy/grunt/blob/master/docs/getting_started.md
  //
  grunt.initConfig({

    // Project configuration
    // ---------------------

    // specify an alternate install location for Bower
    bower: {
      dir: ''
    },

    // Coffee to JS compilation
    coffee: {
      dist: {
        src: 'js/non.coffee',
        dest: 'js/'
      }
    },

    browserify: {
      "js/app.js": {
        entries: ['js/app.coffee'],
        // hook: function(bundle){
        //   bundle.register('post', function (body) {
        //     return require('uglify-js')(body);
        //   });
        // }
      }
    },

    // compile .less to .css using Less
    less: {
      dist: {
        src: 'css/app.less',
        dest: 'css/app.css',
        options: {
          compress: true
        }
      }
    },

    // generate application cache manifest
    manifest: {
      dest: ''
    },

    // default watch configuration
    watch: {
      browserify: {
        files: 'js/**/*.{js,coffee}',
        tasks: 'browserify'
      },
      less: {
        files: [
          'css/**/*.less'
        ],
        tasks: 'less'
      }
    },

    // default lint configuration, change this to match your setup:
    // https://github.com/cowboy/grunt/blob/master/docs/task_lint.md#lint-built-in-task
    lint: {
      files: [
        'Gruntfile.js',
        'js/**/*.js',
        'spec/**/*.js'
      ]
    },

    // specifying JSHint options and globals
    // https://github.com/cowboy/grunt/blob/master/docs/task_lint.md#specifying-jshint-options-and-globals
    jshint: {
      options: {
        curly: true,
        eqeqeq: true,
        immed: true,
        latedef: true,
        newcap: true,
        noarg: true,
        sub: true,
        undef: true,
        boss: true,
        eqnull: true,
        browser: true
      },
      globals: {
        angular: true
      }
    },

    // Build configuration
    // -------------------

    // the staging directory used during the process
    staging: 'temp',
    // final build output
    output: 'dist',

    mkdirs: {
      staging: ''
    },

    // Below, all paths are relative to the staging directory, which is a copy
    // of the app/ directory. Any .gitignore, .ignore and .buildignore file
    // that might appear in the app/ tree are used to ignore these values
    // during the copy process.

    // concat css/**/*.css files, inline @import, output a single minified css
    css: {
      'css/libs.css': ['styles/**/*.css']
    },

    // renames JS/CSS to prepend a hash of their contents for easier
    // versioning
    rev: {
      js: 'scripts/**/*.js',
      css: 'styles/**/*.css',
      img: 'images/**'
    },

    // usemin handler should point to the file containing
    // the usemin blocks to be parsed
    'usemin-handler': {
      html: 'index.html'
    },

    // update references in HTML/CSS to revved files
    usemin: {
      html: ['**/*.html'],
      css: ['**/*.css']
    },

    // HTML minification
    html: {
      files: ['**/*.html']
    },

    // Optimizes JPGs and PNGs (with jpegtran & optipng)
    img: {
      dist: '<config:rev.img>'
    },

    // rjs configuration. You don't necessarily need to specify the typical
    // `path` configuration, the rjs task will parse these values from your
    // main module, using http://requirejs.org/docs/optimization.html#mainConfigFile
    //
    // name / out / mainConfig file should be used. You can let it blank if
    // you're using usemin-handler to parse rjs config from markup (default
    // setup)
    rjs: {
      // no minification, is done by the min task
      optimize: 'none',
      baseUrl: './scripts',
      wrap: true
    },
  });

  // Alias the `test` task to run `testacular` instead
  grunt.registerTask('test', 'run the testacular test driver', function () {
    var done = this.async();
    require('child_process').exec('testacular start --single-run', function (err, stdout) {
      grunt.log.write(stdout);
      done(err);
    });
  });

};
